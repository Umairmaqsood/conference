<h3>First International Conference on Computer Science and Engineering, Islamabad, Pakistan.</h3>
September 29 – 30, 2024
Foundation University Islamabad, School of Science and Technology (FUSST)

Foundation University Islamabad is organizing its first international conference with 
the name of “Fusst International Conference on Computer Science and Engineering (FICSE)” 
from September 29 – 30, 2024 at Foundation University Islamabad, School of Science and Technology. 
The conference will bring together Government, industry, Academia, and civil society to explore the 
role of state of the art technologies in the broad field of Information Technology, electrical, 
instrumentation and communication engineering. FICSE aims to bring together researchers, practitioners
 and experts from academia and industry to focus on recent global advancement.
The conference will mainly include the following sessions:
1.	Research paper presentation sessions
2.	Technology presentation sessions
3.	Discussion sessions
4.	Paid Stalls/ Booths from the concerned stakeholders

Researchers, technologists and engineers from industry, academia and civil society will be invited to submit their proposals/ research papers in response to the “Call for papers” advertised through press/ social media/ website and letters. All the submissions will be made to FICSE portal on or before August 15th, 2024. The submitted items will be scrutinized by Editors for their suitability in terms of innovation, creativity, presentation and relevance. The applicants will be informed about the acceptance of their research item on or before 1st September 2024 after which the presenter will prepare a 20-30 minute presentation for on-site or virtual seminar. The decision of FISCE Editorial on the scrutiny of the research items will be final.
The conference will also include 6 keynote sessions for which the international/national speakers will be invited. The international speakers will make virtual presentations whereas the national speakers will present in the live session.
All the accepted and presented research papers/ workshops/ one-pager of keynote talks will be published in the conference proceedings. All papers will be peer-reviewed and, if accepted, extended version will published in the Foundation University Journal of Engineering and Applied Sciences (“Y-Category” HEC recognized journal).

Every attendee of the conference will be awarded a certificate of participation, a conference kit and a FICSE bag in recognition of his participation in the conference. Best paper/ product award will be given to the presenters scored the highest by the judges of the conference. On every day of the conference, a guest of honor will be invited to distribute the certificates and awards. The MD Fauji Foundation will be the Chief Guest of the conference and will join the opening of the conference on 30th September 2024.
