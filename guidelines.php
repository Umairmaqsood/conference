<h2>2.2 Headings (All headings should be aligned to extreme left of the margin)</h2>
<ul>
    <li>The Initial letter of each notional word in all headings is capitalized.</li>
    <li>The line space between and heading and subsequent text should be 1.5 points.</li>
    <li>Align all headings to extreme left, numbered with decimals. For example:
        <ul>
            <li>The second level two heading under the third level one heading should be numbered as 3.2.</li>
            <li>Level 3 heading, numbered after the level two headings, such as 3.2.1, 3.2.2, etc.</li>
        </ul>
    </li>
</ul>

