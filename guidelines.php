<h2>6. Citations</h2>
<p>Cite references in the main text, by following the IEEE style like [1].</p>

<h2>7. Optional Parts</h2>
<h2 class="optional-heading">Acknowledgement</h2>
<p>The acknowledgements section is your opportunity to thank those who have helped and supported you personally and professionally during your research work process.</p>

<h2 class="optional-heading">Conflict of Interests</h2>
<p>Authors are requested to disclose interests that are directly or indirectly related to the work submitted for publication. Interests within the last 3 years of beginning the work (conducting the research and preparing the work for submission) should be reported.</p>

<h2 class="optional-heading">Funding Statement</h2>
<p>FUJEAS requires authors to specify any sources of funding (institutional, private and corporate financial support) for the work reported in their paper. This information, in the form of the name of the funding organization/s and the grant number -or should be included at the end of the article under the heading ‘Funding’, and provided at the time of submitting the paper. Any suppliers of materials should be named and their location included if appropriate. This information will be included in the published article.</p>

<h2>8. References</h2>
<p>The reference should be given in IEEE format. Avoid unnecessary references, as they create confusion. Keep in mind following points about them:</p>
<ul>
    <li>The facts given a reference should be correct and complete.</li>
    <li>Ensure completeness of critical elements of information (Title, Authors’ Names, Year, Venue, Volume, Issue and Page Numbers).</li>
    <li>All references should be cited in the text.</li>
    <li>Check for duplicate references.</li>
    <li>All references should be in font size 8 and listed at the end of the paper.</li>
    <li>Remember that every resource (conference paper, book or online electronic resource etc.) has some specific data:
        <ul>
            <li>Titles of books and conference are in italics, with the first letter of the notional word capitalized.</li>
            <li>The first letter of Titles of articles, reports, dissertations and conferences should be capitalized.</li>
            <li>References should be arranged in the order in which they appear in the text.</li>
        </ul>
    </li>
</ul>
