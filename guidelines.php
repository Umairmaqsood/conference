<h2>5. Graphs and Charts</h2>
<p>Graphs and Charts also help in better understanding of the manuscript text. Remember following guidelines for them:</p>
<ul>
    <li>Always insert a title (brief but comprehensive) for each graph and chart.</li>
    <li>Adding graph labels, legends and data labels increase their effectiveness.</li>
    <li>Align the graph and chart in center of the page margins.</li>
